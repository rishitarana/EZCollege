import Foundation
import UIKit

func AddRadius(layer: CALayer){
    layer.backgroundColor = UIColor.white.cgColor
    layer.borderWidth = 0.25
    layer.cornerRadius = 15
    layer.borderColor = UIColor.systemGray.cgColor
}
